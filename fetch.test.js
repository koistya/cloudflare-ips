test('fetch(url) should throw an error if status code !== 200', done => {
  expect.assertions(3);
  fetch('https://www.cloudflare.com/ips-xxx', (err, ips) => {
    expect(err).toBeInstanceOf(Error);
    expect(err.message).toBe('HTTP 301: Moved Permanently');
    expect(ips).toBeFalsy();
    done();
  });
});

test('fetch(url) should throw an error if content type !== plain/text', done => {
  expect.assertions(3);
  fetch('https://www.cloudflare.com/', (err, ips) => {
    expect(err).toBeInstanceOf(Error);
    expect(err.message).toMatch(/^Content type is invalid./);
    expect(ips).toBeFalsy();
    done();
  });
});
